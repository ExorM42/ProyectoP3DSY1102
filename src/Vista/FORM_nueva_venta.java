    private void configurarCarrito() {
        modeloCarrito = new DefaultTableModel(
                new Object[]{"ID", "Nombre", "Cant.", "P. Unitario", "Subtotal"}, 0
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; 
            }
        };
        tablaCarrito = new JTable(modeloCarrito);

        
        JScrollPane scrollPane = new JScrollPane(tablaCarrito);

       
        labelTotal = new JLabel("TOTAL: $0.00");
        labelTotal.setFont(new java.awt.Font("Segoe UI", 1, 14));

        jPanel3.setLayout(new BorderLayout());

        jPanel3.add(scrollPane, BorderLayout.CENTER);
        jPanel3.add(labelTotal, BorderLayout.SOUTH);

        jPanel3.revalidate();
        jPanel3.repaint();
    }

    private void actualizarTotal() {
        double total = 0;
        for (int i = 0; i < modeloCarrito.getRowCount(); i++) {
            total += (double) modeloCarrito.getValueAt(i, 4);
        }
        labelTotal.setText(String.format("TOTAL: $%.2f", total));
    }

    private void cargarProductosComboBox() {
        try {
            jmi_Seleccionar.removeAllItems(); 
            productoMap.clear();

            jmi_Seleccionar.addItem("Seleccione un producto");

            List<Producto> productos = prodCRUD.obtenerProductos();

            for (Producto p : productos) {
   
                String displayText = String.format(
                        "ID: %d - %s (Stock: %d)",
                        p.getId_producto(),
                        p.getNombre(),
                        p.getStock()
                );

                jmi_Seleccionar.addItem(displayText);

                productoMap.put(displayText, p);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar productos: " + e.getMessage(), "Error de Conexión", JOptionPane.ERROR_MESSAGE);
            logger.log(java.util.logging.Level.SEVERE, "Error al cargar productos", e);
        }
    }

