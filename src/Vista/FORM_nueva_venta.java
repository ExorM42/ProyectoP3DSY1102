    private void jbtn_AgregarCarritoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_AgregarCarritoActionPerformed
        String itemSeleccionado = (String) jmi_Seleccionar.getSelectedItem();
        if (itemSeleccionado == null || "Seleccione un producto".equals(itemSeleccionado)) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un producto.", "Error de Validación", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String cantidadStr = jtxt_Cantidad.getText().trim();
        int cantidad;
        try {
            cantidad = Integer.parseInt(cantidadStr);
            if (cantidad <= 0) {
                JOptionPane.showMessageDialog(this, "La cantidad debe ser un número positivo.", "Error de Validación", JOptionPane.WARNING_MESSAGE);
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "La cantidad debe ser un número válido.", "Error de Formato", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Producto p = productoMap.get(itemSeleccionado);
        if (p == null) {
            JOptionPane.showMessageDialog(this, "Error: Producto no encontrado en el mapa.", "Error Interno", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (cantidad > p.getStock()) {
            JOptionPane.showMessageDialog(this, 
                "Stock insuficiente. Solo quedan " + p.getStock() + " unidades de " + p.getNombre(), 
                "Error de Stock", 
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        double subtotal = p.getPrecio_venta() * cantidad; 
        modeloCarrito.addRow(new Object[]{
            p.getId_producto(),
            p.getNombre(),
            cantidad,
            p.getPrecio_venta(),
            subtotal
        });
        
        actualizarTotal();
        
        jtxt_Cantidad.setText("");
        jmi_Seleccionar.setSelectedIndex(0);
// TODO add your handling code here:
    }//GEN-LAST:event_jbtn_AgregarCarritoActionPerformed

    private void jbtn_generarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_generarVentaActionPerformed
        int rut = Sesion.getInstancia().getRutUsuario();
        if (modeloCarrito.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "El carrito está vacío. Agregue productos antes de generar la venta.", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Venta venta = new Venta();
        List<Detalle_Venta> detalles = new ArrayList<>();

        double total = Double.parseDouble(labelTotal.getText().replace("TOTAL: $", "").replace(",", "."));
        venta.setRun_vendedor(rut);
        venta.setTotal_Venta((int) total);

        for (int i = 0; i < modeloCarrito.getRowCount(); i++) {
            Detalle_Venta d = new Detalle_Venta();
            d.setId_producto((Integer) modeloCarrito.getValueAt(i, 0));
            d.setCantidad((Integer) modeloCarrito.getValueAt(i, 2));   
            d.setPrecio_unitario((int) modeloCarrito.getValueAt(i, 3)); 
            detalles.add(d);
        }

        try {
            ventCRUD.crearVenta(venta, detalles);

            JOptionPane.showMessageDialog(this,
                    "Venta N° " + venta.getId_venta() + " registrada con éxito.",
                    "Venta Exitosa",
                    JOptionPane.INFORMATION_MESSAGE);

            modeloCarrito.setRowCount(0);
            actualizarTotal();

            cargarProductosComboBox();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                    "Error al registrar la venta:\n" + e.getMessage(),
                    "Error de Venta (CRUD)",
                    JOptionPane.ERROR_MESSAGE);
            logger.log(java.util.logging.Level.SEVERE, "Error CRUD al generar venta", e);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_jbtn_generarVentaActionPerformed

