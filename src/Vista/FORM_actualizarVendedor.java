    private void jtbtn_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jtbtn_ActualizarActionPerformed
        int run_vendedor = 0;
        String nombre = null;
        String segundo_nombre = null;
        String apellido_paterno = null;
        String apellido_materno = null;
        String username = null;
        String administrador = null;

        vendedorCRUD lista = new vendedorCRUD();
        List<Vendedor> lis = lista.listarVendedores();

        DefaultTableModel tabla = (DefaultTableModel) this.jtbl_ActualizarVendedor.getModel();

        tabla.setRowCount(0);

        for (Vendedor li : lis) {

            run_vendedor = li.getRun_vendedor();
            nombre = li.getNombre_vendedor();
            segundo_nombre = li.getSegundo_nombre_vendedor();
            apellido_paterno = li.getApellidoP_Vendedor();
            apellido_materno = li.getApellidoM_vendedor();
            username = li.getUsername();
            if (li.isEs_admin()) {
                administrador = "Y";

            } else {
                administrador = "N";

            }
            tabla.addRow(new Object[]{run_vendedor, nombre, segundo_nombre, apellido_paterno, apellido_materno, username, administrador});

        }


    }//GEN-LAST:event_jtbtn_ActualizarActionPerformed

