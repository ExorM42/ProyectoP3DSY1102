    private void jtbn_elimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jtbn_elimActionPerformed
        vendedorCRUD vendC = new vendedorCRUD();
        int fila = this.jtbl_ActualizarVendedor.getSelectedRow();
        if (fila == -1){
            JOptionPane.showMessageDialog(this,"Debe seleccionar una fila","Actualizar",2);
               
        }
                
        String rut = this.jtbl_ActualizarVendedor.getValueAt(fila, 0).toString();
        int rut1 = Integer.parseInt(rut);
        
        int confirmar = JOptionPane.showConfirmDialog(this, "Â¿Seguro que desea eliminar al vendedor con RUT " + rut1 + "?", "CONFIRMACION", 2);
        if (confirmar == 0) {
            vendC.eliminarVendedor(rut1);
            
        }else{
            JOptionPane.showMessageDialog(this, "Se aborto la eliminacion del vendedor", "MENSAJE", 1);
        }


    }//GEN-LAST:event_jtbn_elimActionPerformed

