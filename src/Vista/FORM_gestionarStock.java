    private void jbtn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_buscarActionPerformed

        int id = 0;
        int precio = 0;
        int stock = 0;
        int id_int = 0;

        boolean flag = false;
        productoCRUD prodC = new productoCRUD();
        List<Producto> listas = prodC.obtenerProductos();

        try {
            id_int = Integer.parseInt(this.jtxt_buscarprod.getText());
            for (Producto lista : listas) {
                if (id_int == lista.getId_producto()) {
                    flag = true;
                    
                    
                }

            }
        } catch (NumberFormatException e) {
            System.out.println("debe ser numero");
        }


        DefaultTableModel modelo = (DefaultTableModel) this.jtbl_producto.getModel();

        try {
            id = Integer.parseInt(this.jtxt_buscarprod.getText());

        } catch (NumberFormatException e) {
            id = 0;
            System.out.println("Numerico" + e.getMessage());
        }

        modelo.setRowCount(0);

        if (id == 0) {

            List<Producto> lista = prodC.obtenerProductos();

            for (Producto prods : lista) {

                id = prods.getId_producto();
                String nombre_producto = prods.getNombre();
                String tipo = prods.getTipo_producto();
                precio = prods.getPrecio_venta();
                stock = prods.getStock();

                modelo.addRow(new Object[]{id, nombre_producto, tipo, precio, stock});

            }

        } else if (flag){
            Producto prod = prodC.productoPorId(id);
            id = prod.getId_producto();
            String nombre_producto = prod.getNombre();
            String tipo = prod.getTipo_producto();
            precio = prod.getPrecio_venta();
            stock = prod.getStock();

            modelo.addRow(new Object[]{id, nombre_producto, tipo, precio, stock});

        }else{
            JOptionPane.showMessageDialog(this, "El ID ingresado no existe en la base de datos", "ERROR", 2);
            this.jtxt_buscarprod.setText("");
            this.jtxt_buscarprod.requestFocus();
        }


    }//GEN-LAST:event_jbtn_buscarActionPerformed

