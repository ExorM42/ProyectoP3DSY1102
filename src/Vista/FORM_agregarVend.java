    private boolean uservend (String username){
        vendedorCRUD vendC = new vendedorCRUD();
        List<Vendedor> vend = new ArrayList<>();
        vend = vendC.listarVendedores();
        boolean flag = false;
        
        for (Vendedor vendedor : vend) {
            
            if (username.equals(vendedor.getUsername())) {
                flag = true;
                
            }
            
        }
        return flag;
    }

