    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        boolean admin = false;
        int run_vendedor = 0;
        char[] passwordArray = this.jpw_pass.getPassword();
        String passw = new String(passwordArray);
        String apellidoM_vendedor = this.jtxt_amaterno.getText();
        String apellidoP_Vendedor = this.jtxt_apaterno.getText();
        String username = this.jtxt_user.getText();
        String dv_rut_vendedor = this.jtxt_rut_dv.getText();
        String nombre_vendedor = this.jtxt_nombre.getText();
        String segundo_nombre_vendedor = this.jtxt_snombre.getText();
        char[] passwordArray1 = this.jpw_confirmar.getPassword();
        String passw1 = new String(passwordArray1);
        String rut = this.jtxt_rut_dosPrimeros.getText();
        vendedorCRUD vendC = new vendedorCRUD();
        if (!passw1.equals(passw)) {
            JOptionPane.showMessageDialog(this, "Contraseñas deben coincidir", "Error de contraseña", 2);
            throw new IllegalArgumentException("Contraseña no coincide");
        }
        
        if (this.jrbtn_si.isSelected()){
            admin = true;
        }

        if (nombre_vendedor.equals("")) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un nombre", "ERROR", 2);
            this.jtxt_nombre.requestFocus();
            throw new IllegalArgumentException("No ingresa nombre");
        }

        if (username.equals("")) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un nombre de usuario", "Error de ingreso", 2);
            this.jtxt_user.requestFocus();
            throw new IllegalArgumentException("No ingresa username");
        }

        if (dv_rut_vendedor.equals("")) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un digito verificador en el rut", "Error de ingreso", 2);
            this.jtxt_rut_dv.requestFocus();
            throw new IllegalArgumentException("No ingresa digito verificador");
        }
        if (segundo_nombre_vendedor.equals("")) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un segundo nombre", "Error de ingreso", 2);
            this.jtxt_snombre.requestFocus();
            throw new IllegalArgumentException("No ingresa segundo nombre");
        }

        if (apellidoM_vendedor.equals("")) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un apellido materno", "Error de ingreso", 2);
            this.jtxt_apaterno.requestFocus();
            throw new IllegalArgumentException("No ingresa apellido materno");
        }

        if (rut.length() > 12) {
            JOptionPane.showMessageDialog(this, "La longitud del rut debe ser menor o igual a 12", "Error de ingreso", 2);
            this.jtxt_rut_dosPrimeros.requestFocus();
            throw new IllegalArgumentException("RUT 12");
        }
        if (apellidoP_Vendedor.equals("")) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un apellido paterno", "Error de ingreso", 2);
            throw new IllegalArgumentException("no ingreso nada");
        }
        
        if (this.uservend(username)){
            JOptionPane.showMessageDialog(this, "El username debe ser unico", "Error de ingreso", 2);
            this.jtxt_user.requestFocus();
            this.jtxt_user.setText("");
            throw new IllegalArgumentException("Usuario repetido");
        }
        try {
            run_vendedor = Integer.parseInt(this.jtxt_rut_dosPrimeros.getText());
            List<Vendedor> lista = new ArrayList<>();
            lista = vendC.listarVendedores();
            for (Vendedor vendedor : lista) {
                if (run_vendedor == vendedor.getRun_vendedor()) {
                    JOptionPane.showMessageDialog(this, "Ya existe un usuario con este rut", "Error de ingreso", 2);
                    throw new IllegalArgumentException("rut ya existe");
                    
                }
                
            }
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El rut debe ser numérico", "Ingreso RUT", 2);
            this.jtxt_rut_dosPrimeros.requestFocus();
            throw new IllegalArgumentException("rut debe ser numerico");
        }
        
       


        Vendedor vendedor = new Vendedor(run_vendedor, dv_rut_vendedor, username, nombre_vendedor, segundo_nombre_vendedor, apellidoP_Vendedor, apellidoM_vendedor, admin);
        vendedorCRUD vendc = new vendedorCRUD();

        if (vendc.agregarVendedor(vendedor, passw)) {

            JOptionPane.showMessageDialog(this, "Se agregó el vendedor", "Ingreso Vendedor", 1);

        } else {
            JOptionPane.showMessageDialog(this, "No se pudo agregar Vendedor", "Vendedor no Ingresado", 2);

        }
        


    }//GEN-LAST:event_jButton1ActionPerformed

